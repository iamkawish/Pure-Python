def add(num1,num2):
    """Docstring:
add(value1,value2)

Sums the values.
return and numerical value incase of integers are supplied 
return string value in case of string are supplied"""
    return num1+ num2


def sub(num3,num4):
    """Docstring:
add(value1,value2)

Subtract the values.
return and numerical value incase of integers are supplied 
return string value in case of string are supplied"""
    return num3-num4


