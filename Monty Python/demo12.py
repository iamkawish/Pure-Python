#Assignment Operator
x= 20
y=10
x+=2 # x=x+2 ==>20+2 ==>22
x+=y # x = x+y ==> 20+10 ==>30
x-=5 # x=x-5 ==>x=10-5 ==>5
x-=y # x = x-y ==> 20-10 ==>10
x*=50 #x=x*50 ==> x=20*50 ==> 1000
x*=y #x=x*y ==>x=20*10 ==>200
x/=100 #x=x/100 ==> 200/100 ==>2.0
x//=y #x=x//y ==> x=200//10 ==>20 






